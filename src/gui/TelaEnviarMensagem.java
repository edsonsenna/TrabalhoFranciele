    private void iniciaUsuarios() {
        UsuarioDAO usuarioDAO = new UsuarioDAO();
        ArrayList<Usuario> usuarios = new ArrayList();
        usuarios = usuarioDAO.consultar();
        for(Usuario usuario: usuarios){
            this.jComboBox1.addItem(usuario.getNome());
        }
    }
