    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        // TODO add your handling code here:
        UsuarioDAO usuarioDAO = new UsuarioDAO();
        MensagemDAO mensagemDAO = new MensagemDAO();
        ArrayList<Usuario> usuarios = new ArrayList();
        String mensagem = txtMensagem.getText();
        usuarios = usuarioDAO.consultar(jComboBox1.getSelectedItem().toString());
        int idDest = usuarios.get(0).getCode();
        int idRem = this.getCodUsuario();
        mensagemDAO.enviarMensagem(idRem, idDest, mensagem);
        JOptionPane.showMessageDialog(null, "Mensagem enviada!");
        TelaPrincipal p = new TelaPrincipal(this.getCodUsuario());
        p.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnEnviarActionPerformed

